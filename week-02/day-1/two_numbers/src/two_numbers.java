public class two_numbers {
    public static void main(String[] args) {
        int no1 = 22;
        int no2 = 13;
        System.out.println(no1+no2);
        System.out.println(no2-no1);
        System.out.println(no1*no2);
        System.out.println(22.0/no2);
        System.out.println(no1/no2);
        System.out.println(no1%no2);
    }
}
